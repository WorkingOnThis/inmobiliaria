import { NextRequest, NextResponse } from "next/server";
import { headers } from "next/headers";
import { db } from "@/db";
import { tenantLedger } from "@/db/schema/tenant-ledger";
import { contract } from "@/db/schema/contract";
import { property } from "@/db/schema/property";
import { client } from "@/db/schema/client";
import { auth } from "@/lib/auth";
import { canManageClients } from "@/lib/permissions";
import { requireAgencyId, requireAgencyResource, handleAgencyError } from "@/lib/auth/agency";
import { and, eq, inArray } from "drizzle-orm";
import { computeNetAndCommission, round2 } from "@/lib/owners/commission";
import { cajaMovimiento } from "@/db/schema/caja";

type EnrichedEntry = {
  id: string;
  contratoId: string;
  inquilinoId: string;
  propietarioId: string;
  propiedadId: string;
  period: string | null;
  dueDate: string | null;
  tipo: string;
  descripcion: string;
  monto: string | null;
  estado: string;
  installmentOf: string | null;
  reciboNumero: string | null;
  montoPagado: string | null;
  ultimoPagoAt: string | null;
  cancellationReason: string | null;
  isAutoGenerated: boolean;
  impactaPropietario: boolean;
  incluirEnBaseComision: boolean;
  impactaCaja: boolean;
  beneficiario: string | null;
  splitBreakdown: string | null;
  isSynthetic?: boolean;
  cashMovementId: string | null;
};

export async function GET(
  _request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const session = await auth.api.getSession({ headers: await headers() });
    const agencyId = requireAgencyId(session);
    if (!canManageClients(session!.user.role)) return NextResponse.json({ error: "Sin permisos" }, { status: 403 });

    const { id: propietarioId } = await params;

    await requireAgencyResource(client, propietarioId, agencyId);

    const currentYear = new Date().getFullYear().toString();

    // Fetch entries joined with contract for managementCommissionPct
    const rawRows = await db
      .select({
        entry: tenantLedger,
        managementCommissionPct: contract.managementCommissionPct,
      })
      .from(tenantLedger)
      .innerJoin(contract, eq(tenantLedger.contratoId, contract.id))
      .where(
        and(
          eq(tenantLedger.agencyId, agencyId),
          eq(tenantLedger.propietarioId, propietarioId),
          eq(tenantLedger.impactaPropietario, true)
        )
      )
      .orderBy(tenantLedger.period, tenantLedger.tipo);

    // Map reciboNumero → primary cashMovementId (the agency income movement)
    // Each receipt creates 3 cash_movements; we pick the one with categoria="alquiler"
    // and tipoFondo="agencia" to use as the canonical comprobante target.
    const conciliatedReciboNumeros = Array.from(
      new Set(
        rawRows
          .filter(({ entry }) => entry.estado === "conciliado" && entry.reciboNumero)
          .map(({ entry }) => entry.reciboNumero!)
      )
    );

    const cashMovementByRecibo = new Map<string, string>();
    if (conciliatedReciboNumeros.length > 0) {
      const cashRows = await db
        .select({
          id: cajaMovimiento.id,
          reciboNumero: cajaMovimiento.reciboNumero,
        })
        .from(cajaMovimiento)
        .where(
          and(
            eq(cajaMovimiento.agencyId, agencyId),
            inArray(cajaMovimiento.reciboNumero, conciliatedReciboNumeros),
            eq(cajaMovimiento.categoria, "alquiler"),
            eq(cajaMovimiento.tipoFondo, "agencia")
          )
        );
      for (const row of cashRows) {
        if (row.reciboNumero) cashMovementByRecibo.set(row.reciboNumero, row.id);
      }
    }

    // Fetch unique properties
    const propertyRows = await db
      .selectDistinct({ id: property.id, address: property.address })
      .from(property)
      .innerJoin(tenantLedger, eq(tenantLedger.propiedadId, property.id))
      .where(
        and(
          eq(tenantLedger.agencyId, agencyId),
          eq(tenantLedger.propietarioId, propietarioId),
          eq(tenantLedger.impactaPropietario, true)
        )
      );

    // Build enriched entries with synthetic honorarios rows interleaved
    const ledgerEntries: EnrichedEntry[] = [];
    let totalLiquidadoYTD = 0;
    let totalPendiente = 0;

    for (const { entry, managementCommissionPct } of rawRows) {
      const pct = Number(managementCommissionPct ?? 10);
      const { net, commission, effectivePct } = computeNetAndCommission(entry, pct);

      // Add the original entry (gross monto stays as-is)
      ledgerEntries.push({
        ...entry,
        cashMovementId: entry.reciboNumero
          ? cashMovementByRecibo.get(entry.reciboNumero) ?? null
          : null,
      });

      // Sum nets for KPIs
      const isCurrentYear = entry.dueDate?.startsWith(currentYear) ?? false;
      if (entry.estado === "conciliado" && isCurrentYear) {
        totalLiquidadoYTD += net;
      } else if (entry.estado === "pendiente" || entry.estado === "registrado") {
        totalPendiente += net;
      }

      // Add synthetic honorarios row when commission > 0
      if (commission > 0) {
        ledgerEntries.push({
          id: `synthetic-honorarios-${entry.id}`,
          contratoId: entry.contratoId,
          inquilinoId: entry.inquilinoId,
          propietarioId: entry.propietarioId,
          propiedadId: entry.propiedadId,
          period: entry.period,
          dueDate: entry.dueDate,
          tipo: "honorarios_admin",
          descripcion: `Honorarios administración (${effectivePct}%)`,
          monto: String(-commission),
          estado: entry.estado,
          installmentOf: null,
          reciboNumero: entry.reciboNumero,
          montoPagado: null,
          ultimoPagoAt: null,
          cancellationReason: null,
          isAutoGenerated: true,
          impactaPropietario: true,
          incluirEnBaseComision: false,
          impactaCaja: false,
          beneficiario: null,
          splitBreakdown: null,
          isSynthetic: true,
          cashMovementId: null,
        });
      }
    }

    return NextResponse.json({
      kpis: {
        totalLiquidadoYTD: round2(totalLiquidadoYTD),
        totalPendiente: round2(totalPendiente),
      },
      ledgerEntries,
      properties: propertyRows,
    });
  } catch (error) {
    const resp = handleAgencyError(error);
    if (resp) return resp;
    console.error("Error GET /api/owners/:id/cuenta-corriente:", error);
    return NextResponse.json({ error: "Error al obtener la cuenta corriente" }, { status: 500 });
  }
}
